                        <h2>Algo que contar</h2>
                        <p>Cuenta la historia… que hace 73 años en una lejana provincia llamada Antioquia, comenzaba a
                            escribirse uno de los más brillantes capítulos del Fútbol profesional en Colombia.


                            Un grupo de visionarios fundaban, contra viento y marea, el Club Atlético Municipal, uno de
                            los dos equipos, que en aquel entonces, representarían a la región en el torneo de la
                            DIMAYOR.


                            Se necesitaron 500 guerreros que creyeran en esta cruzada, con un aporte voluntario de $100
                            pesos cada uno, para iniciar lo que sería el camino de la gloria y la grandeza.


                            Basados en su principio filosófico de creación «Por encima de todo la defensa y estimulo del
                            jugador nacional» modificaron su nombre para 1950, surgiendo para la historia eterna
                            Atlético Nacional.


                            Y desde allí hasta el día de hoy, muchas cosas han pasado, triunfos, derrotas, títulos,
                            personajes, anécdotas.


                            Por eso hoy, apoyados en un grupo de historiadores, curadores, exjugadores, ex directivos y
                            colaboradores, queremos manifestar nuestra firme intención de reconstruir las historias, los
                            momentos y los recuerdos vividos en estos 73 años de vida institucional.


                            Y que mejor escenario que el anhelado Museo para reunir allí todos aquellos objetos que nos
                            traen remembranzas y sentimientos, los que sin duda tenemos la obligación de pasar de
                            generación en generación, para hacer aún más grande la historia del Verde.


                            Más de 11 millones de guerreros verdolagas, de hinchas fieles a nuestra causa, nos motivan
                            hoy a iniciar un proyecto ambicioso que necesita del aporte de mucha gente para hacerlo
                            realidad. Estamos tocando la puerta de todos aquellos que tienen en sus manos prendas,
                            objetos, impresos históricos relacionados con Atlético Nacional que puedan llegar a nuestro
                            Museo.


                            Somos conscientes que no es fácil desprenderse de objetos valiosos o representativos en lo
                            sentimental, pero creemos que esta iniciativa vale la pena, porque sería dejar para el Club
                            y sus hinchas un legado que será admirado por millones de personas en Antioquia, en Colombia
                            y en el mundo.


                            Hoy damos inicio a la construcción de un sueño ambicioso que, como tal, se deberá construir
                            por fases. Sin prisa pero sin pausa buscamos recorrer una ruta que inicie desde la curaduría
                            de las colecciones asociadas a nuestra historia, pase por el espacio de los recorridos
                            virtuales, nos permita compartir nuestro recorrido en formatos itinerantes y llegue a ese
                            puerto tan anhelado de un espacio físico permanente que sea orgullo de nuestra hinchada,
                            ciudad y país


                            Bienvenidos todos los jugadores, integrantes de los diferentes cuerpos técnicos, directivas,
                            familiares, personal administrativo, coleccionistas, comunicadores y periodistas, barristas
                            y amantes de este gran sentimiento llamado Atlético Nacional, sus objetos quedarán para
                            siempre en la historia Verdolaga.


                            Nos encontrarán a partir de hoy en las redes:


                            Instagram: @museoatleticonacional


                            Facebook: Museo Atlético Nacional</p>

